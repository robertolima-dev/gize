#[cfg(test)]
mod tests {
    use super::*;

    fn product() -> ModelSpec {
        ModelSpec::parse(
            "Product",
            &[
                "name:String".to_string(),
                "price:i32".to_string(),
                "active:bool".to_string(),
            ],
        )
        .unwrap()
    }

    #[test]
    fn dto_has_all_fields() {
        let out = dto_rs(&product());
        assert!(out.contains("pub struct CreateProduct"));
        assert!(out.contains("pub struct UpdateProduct"));
        assert!(out.contains("pub name: String,"));
        assert!(out.contains("pub price: i32,"));
        assert!(out.contains("pub active: bool,"));
    }

    #[test]
    fn repository_builds_sql() {
        let out = repository_rs(&product());
        assert!(out.contains("INSERT INTO products (name, price, active) VALUES ($1, $2, $3)"));
        assert!(
            out.contains(
                "SET name = $1, price = $2, active = $3, updated_at = now() WHERE id = $4"
            )
        );
        assert!(out.contains(".bind(input.name.clone())"));
        assert!(out.contains(".bind(id)"));
    }

    #[test]
    fn handler_and_routes_reference_resource() {
        let handler = handler_rs(&product());
        assert!(handler.contains("Json<Vec<Product>>"));
        let routes = routes_rs(&product());
        assert!(routes.contains("\"/products\""));
        assert!(routes.contains("\"/products/:id\""));
    }
}
